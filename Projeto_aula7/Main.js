function sortearCarta() {
    let numeroCartaMaquina = parseInt(Math.random() * 11)
    cartaMaquina = cartaAndroxus[numeroCartaMaquina]

    let numeroCartaJogador = parseInt(Math.random() * 11)
    while (numeroCartaJogador == numeroCartaMaquina) {
        numeroCartaJogador = parseInt(Math.random() * 11)
    }
    cartaJogador = cartas[numeroCartaJogador]
    document.querySelector('#carta').innerHTML = `<div class='player1'><h2>${cartaJogador.nome}</h2><img src="${cartaJogador.url}"><p>Ataque: ${cartaJogador.atributos.ataque}</p><p>Defesa: ${cartaJogador.atributos.defesa}</p><p>Magia: ${cartaJogador.atributos.magia}</p></div>`

    document.querySelector('#btnSortear').disabled = true
    //document.querySelector('#btnJogar').disabled = false

    exibirOpcoes()
}

