function validaSkill(src) {
    let valor
    if(src.endsWith('Attack.png')) {
        valor = 'ataque'
    } else if(src.endsWith('background.jpg')) {
        valor = 'defesa'
    } else {
        valor = 'magia'
    }
    return valor
}

